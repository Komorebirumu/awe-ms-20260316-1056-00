def generate_episode_ideas(niche, num_ideas=5, audience_focus=None):
    """Generates podcast episode ideas based on niche and audience focus."""
    if niche not in TRENDING_TOPICS:
        return None, None, None

    available_topics = TRENDING_TOPICS[niche]
    available_engagement = AUDIENCE_ENGAGEMENT.get(niche, {})

    # Prioritize topics related to audience_focus if specified and available
    relevant_topics = []
    if audience_focus and audience_focus in available_engagement:
        relevant_topics.extend(available_engagement[audience_focus])
    # Add general topics if audience_focus is not specific or not found
    relevant_topics.extend(available_engagement.get("general", []))

    # Combine trending topics with engagement-focused topics, avoiding duplicates
    combined_ideas = list(set(available_topics + relevant_topics))
    random.shuffle(combined_ideas)

    # Select a subset of topics for episode ideas
    selected_topics = combined_ideas[:num_ideas]

    episode_suggestions = []
    for i, topic in enumerate(selected_topics):
        # Generate a catchy title
        title_template = random.choice([
            f"Exploring {topic.title()}",
            f"The Future of {topic.title()}",
            f"{topic.title()}: Challenges and Innovations",
            f"Deep Dive into {topic.title()}",
            f"Unpacking {topic.title()} for [Your Audience]" # Placeholder for specific audience
        ])
        title = title_template.replace("[Your Audience]", niche)
        episode_suggestions.append(f"{i+1}. Episode Title: {title}")

    # Suggest potential guests and questions
    guest_data = GUEST_PROFILES_QUESTIONS.get(niche)
    if guest_data:
        guest_suggestions = []
        num_guests_to_suggest = min(len(guest_data["guest_archetypes"]), 2) # Suggest up to 2 guests
        suggested_guests = random.sample(guest_data["guest_archetypes"], num_guests_to_suggest)
        for guest in suggested_guests:
            guest_suggestions.append(f"- Potential Guest Profile: {guest}")

        interview_questions_suggestions = []
        num_questions_to_suggest = min(len(guest_data["questions"]), 3) # Suggest up to 3 questions
        suggested_questions = random.sample(guest_data["questions"], num_questions_to_suggest)
        for question in suggested_questions:
            interview_questions_suggestions.append(f"- Sample Interview Question: {question}")
    else:
        guest_suggestions = ["- No specific guest profiles available for this niche."]
        interview_questions_suggestions = ["- No specific interview questions available for this niche."]

    return episode_suggestions, guest_suggestions, interview_questions_suggestions


